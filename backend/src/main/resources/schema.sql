CREATE TABLE IF NOT EXISTS TB_MENU (
    MENU_PK BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MENU_ID VARCHAR(100) NOT NULL,
    PARENT_PK BIGINT,
    DEPTH_LEVEL INT NOT NULL,
    MENU_TYPE VARCHAR(30) NOT NULL,
    ROUTE_PATH VARCHAR(255),
    SORT_ORDER INT NOT NULL,
    IS_VISIBLE VARCHAR(1) NOT NULL,
    IS_ENABLED VARCHAR(1) NOT NULL,
    ICON VARCHAR(100),
    CREATED_AT TIMESTAMP NOT NULL,
    CREATED_BY VARCHAR(20) NOT NULL,
    UPDATED_AT TIMESTAMP,
    UPDATED_BY VARCHAR(20),
    CONSTRAINT UK_TB_MENU_MENU_ID UNIQUE (MENU_ID),
    CONSTRAINT FK_TB_MENU_PARENT FOREIGN KEY (PARENT_PK) REFERENCES TB_MENU (MENU_PK)
);

CREATE INDEX IF NOT EXISTS IDX_TB_MENU_PARENT_PK ON TB_MENU (PARENT_PK);
CREATE INDEX IF NOT EXISTS IDX_TB_MENU_SORT_ORDER ON TB_MENU (SORT_ORDER);
